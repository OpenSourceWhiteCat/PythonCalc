a = float(input("enter the first number "))
op = input("enter the oprator (+,-,*,/)")
b = float(input("enter the second number "))

if op == '+':
    print("answer",a+b)
elif op == '-':
    print("answer",a-b) 
elif op == '*':
    print("answer",a*b)
elif op == '/':
    print("answer",a/b)  
else:
    print("Error : invalid oprator ")







